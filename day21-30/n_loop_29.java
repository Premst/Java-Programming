
// Java Nested for loop

public class n_loop_29 {
    public static void main(String[] args) {

        // print pyramid pattern 

        for(int i=0; i<5; i++){
            for(int j=0; j<i; j++){
                System.out.print("*"+" ");
            }
            System.out.println(); // new line
        }
    }
}

public class for_loop_29 {
    public static void main(String[] args) {
        // print 1 to 10
        for(int i = 1; i<=10; i++){
            System.out.println(i);
        }

        // add 1 to 10
        int s = 0;
        for(int i=1; i<=10; i++){
            s = s+i;
        }
        System.out.println("additon of 1 to 10 : " + s);
    }
}
